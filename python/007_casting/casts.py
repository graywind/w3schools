x = int(1)    # x will be 1
y = int(2.8)  # y will be 2
z = int("3")  # z will be 3

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))

x = float(1)      # x will be 1.0
y = float(2.8)    # y will be 2.8
z = float("3")    # z will be 3.0
w = float("4.2")  # w will be 4.2

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))
print(w)
print(type(w))

x = str("s1")  # x will be 's1'
y = str(2)     # y will be '2'
z = str(3.0)   # z will be '3.0'

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))
